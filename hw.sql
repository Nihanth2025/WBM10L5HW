SELECT CUSTOMER.CUST_NAME, EMPLOYEE.NAME, EMPLOYEE.CITY
FROM CUSTOMER 
JOIN EMPLOYEE ON CUSTOMER.CITY = EMPLOYEE.CITY;

SELECT CUSTOMER.CUST_NAME, EMPLOYEE.NAME
FROM CUSTOMER
JOIN EMPLOYEE ON CUSTOMER.EMPLOYEE_ID = EMPLOYEE.EMPLOYEE_ID;

SELECT SALES.ORD_NO, CUSTOMER.CUST_NAME, SALES.CUSTOMER_ID, SALES.EMPLOYEE_ID
FROM SALES
JOIN CUSTOMER ON SALES.CUSTOMER_ID = CUSTOMER.CUSTOMER_ID
JOIN EMPLOYEE ON SALES.EMPLOYEE_ID = CUSTOMER.EMPLOYEE_ID  
WHERE CUSTOMER.CITY <> EMPLOYEE.CITY;

SELECT SALES.ORD_NO, CUSTOMER.CUST_NAME
FROM SALES
JOIN CUSTOMER ON SALES.CUSTOMER_ID = CUSTOMER.CUSTOMER_ID;

SELECT CUSTOMER.CUST_NAME AS "Customer", CUSTOMER.GRADE AS "Grade"
FROM SALES
JOIN EMPLOYEE ON SALES.EMPLOYEE_ID = EMPLOYEE.EMPLOYEE_ID
JOIN CUSTOMER ON SALES.CUSTOMER_ID = CUSTOMER.CUSTOMER_ID
WHERE CUSTOMER.GRADE IS NOT NULL

SELECT CUSTOMER.CUST_NAME AS "Customer",
CUSTOMER.CITY AS "City",
EMPLOYEE.NAME AS "EMPLOYEE",
EMPLOYEE.SALARY
FROM CUSTOMER
JOIN EMPLOYEE ON CUSTOMER.EMPLOYEE_ID = EMPLOYEE.EMPLOYEE_ID
WHERE EMPLOYEE.SALARY BETWEEN 30000 AND 210000;

SELECT SALES.ORD_NO, CUSTOMER.CUST_NAME, EMPLOYEE.SALARY AS "SALARY%",
SALES.PURCH_AMT * EMPLOYEE.SALARY AS "SALARY"
FROM SALES
JOIN EMPLOYEE ON SALES.EMPLOYEE_ID = EMPLOYEE.EMPLOYEE_ID
JOIN CUSTOMER ON SALES.CUSTOMER_ID = CUSTOMER.CUSTOMER_ID
WHERE CUSTOMER.GRADE >= 200;

